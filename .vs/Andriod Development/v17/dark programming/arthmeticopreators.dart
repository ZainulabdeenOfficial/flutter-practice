void main()
{
    int a = 2;
    int b = 3;

    
    var c = a + b;
    print("Sum  (a + b) = $c");

    var d = a - b;
    print("Difference (a - b) = $d");

   
    var e = -d;
    print("Negation -(a - b) = $e");

    var f = a * b;
    print("Product (a * b) = $f");

  
    var g = b / a;
    print("Division (b / a) = $g");

    
    var h = b ~/ a;
    print("Quotient (b ~/ a) = $h");

   
    var i = b % a;
    print("Remainder (b % a) = $i");
}
