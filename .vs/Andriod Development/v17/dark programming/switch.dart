void main() {
  int n = 6;
  switch (n) {
    case 1:
      print('number is one');
      break;
    case 2:
      print('number is two');
      break;
    case 3:
      print('number is three');
      break;
    case 4:
      print('number is four');
      break;
    case 5:
      print('number is five');
      break;
    default:
      print('number is greater than five');
      break;
  }
}
